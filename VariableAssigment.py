# variable assignment
x = 10          # integer
y = 3.5         # float
name = "Uday"   # string
is_student = True  # boolean

print(x, y, name, is_student)



#Key points:

#No need to declare type (int, float, etc.) – Python is dynamically typed.

#Variable names:

#Can contain letters, digits, _

#Cannot start with a digit

#Case-sensitive (age ≠ Age)